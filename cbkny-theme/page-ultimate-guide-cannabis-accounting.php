      <h2 style="color: var(--cbkny-black); font-size: 2rem; margin-bottom: 1.5rem;">2. Understanding 280E Tax Code</h2>
      
      <p>Section 280E of the Internal Revenue Code is perhaps the most significant challenge facing cannabis businesses. This federal law prohibits businesses that traffic in controlled substances from deducting most business expenses, dramatically increasing their tax burden.</p>
      
      <h3 style="color: var(--cbkny-black); margin: 2rem 0 1rem 0;">What 280E Means for Your Business</h3>
      <p>Under 280E, cannabis businesses can only deduct:</p>
      <ul>
        <li>Cost of Goods Sold (COGS)</li>
        <li>Directly related expenses (very limited)</li>
      </ul>
      
      <p>This means most traditional business deductions are off-limits, including:</p>
      <ul>
        <li>Marketing and advertising</li>
        <li>General administrative expenses</li>
        <li>Most professional services</li>
        <li>Rent and utilities (in most cases)</li>
      </ul>
      
      <div class="warning-box" style="background: #fff3cd; border: 1px solid #ffeaa7; padding: 2rem; border-radius: 0.5rem; margin: 2rem 0;">
